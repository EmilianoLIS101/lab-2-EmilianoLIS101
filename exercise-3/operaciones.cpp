/* 1. ¿Cuáles son los resultados de las siguientes expresiones?

   | Operación  | Resultado |
   | ---------- | --------- |
   | 24 / 5     |     4     |
   | 18 % 3     |           |
   | 13 % 9     |           |
   | 13 / 2 % 2 |           | */

#include <iostream>

using namespace std;

int main() {
    cout << "Resultados de las expresiones:" << endl;
    cout << "24 / 5 = " << 24 / 5 << endl;
    cout << "18 % 3 = " << 18 % 3 << endl;
    cout << "13 % 9 = " << 13 % 9 << endl;
    cout << "13 / 2 % 2 = " << 13 / 2 % 2 << endl;

    return 0;
}

